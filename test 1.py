
#To calculate simple interest for the given principle amount
    
def simple_interest(a,b,c):
    print('The principal amount ', a)
    print('The time taken ', b)
    print('The rate of interest ',c)
	
    si = (a * b * c)/100
	
    print('The Simple Interest is', si)

